use anyhow::{anyhow, Result};
use serde_json::Value;

pub async fn patterns(args: Value) -> Result<String> {
    let pattern = args.get("pattern").and_then(|v| v.as_str()).filter(|s| !s.is_empty()).unwrap_or("");

    if pattern == "list" || pattern.is_empty() {
        return Ok(r#"Доступные паттерны форм 1С:

1. form-document — Форма документа
2. form-data-processor — Форма обработки
3. form-list — Форма списка
4. form-catalog-item — Форма элемента справочника
5. form-wizard — Мастер

Используйте pattern=form-document (или другое имя) для деталей."#.to_string());
    }

    match pattern {
        "form-document" => Ok(r#"=== Форма документа ===

ASCII-схема:
┌──────────────────────────────────────┐
│ [КоманднаяПанель]                    │
├──────────────────────────────────────┤
│ ГруппаШапка                          │
│ ┌─────────┬────────────────────────┐ │
│ │ Номер:  │ [Номер]    Дата:[Дата] │ │
│ ├─────────┼────────────────────────┤ │
│ │ Организ:│ [Организация]          │ │
│ └─────────┴────────────────────────┘ │
├──────────────────────────────────────┤
│ ГруппаТЧ                             │
│ [Таблица товаров с команд.панелью]   │
├──────────────────────────────────────┤
│ ГруппаПодвал                         │
│ ┌─────────┬────────────────────────┐ │
│ │ Итого:  │ Сумма документа: 1000 │ │
│ └─────────┴────────────────────────┘ │
└──────────────────────────────────────┘

События: ПриОткрытии, ПередЗаписью, ПриЗаписи, ОбработкаПроведения
Свойства: Нумерация, Проведение, Итоги"#.to_string()),

        "form-data-processor" => Ok(r#"=== Форма обработки ===

ASCII-схема:
┌──────────────────────────────────────┐
│ [КоманднаяПанель]                    │
├──────────────────────────────────────┤
│ ГруппаНастройки                      │
│ ┌──────────────────────────────────┐ │
│ │ Период: с [ДатаНач] по [ДатаКон]│ │
│ │ [Выполнить]                      │ │
│ └──────────────────────────────────┘ │
├──────────────────────────────────────┤
│ [Результат] (таблица или текст)     │
├──────────────────────────────────────┤
│ ГруппаСтатус                         │
│ ┌──────────────────────────────────┐ │
│ │ Статус: Выполнено               │ │
│ └──────────────────────────────────┘ │
└──────────────────────────────────────┘

События: ПриОткрытии, ПередЗаписью, ПриЗакрытии
Рекомендации: Использовать реквизит Команда для действий"#.to_string()),

        "form-list" => Ok(r#"=== Форма списка ===

ASCII-схема:
┌──────────────────────────────────────┐
│ [КоманднаяПанель (Создать, Найти)]   │
├──────────────────────────────────────┤
│ [Таблица / ДинамическийСписок]       │
│ ┌────┬──────────┬─────────┬────────┐│
│ │ #  │ Наименов │ Код     │ ...   ││
│ ├────┼──────────┼─────────┼────────┤│
│ │ 1  │ Товар1   │ 00001   │ ...   ││
│ │ 2  │ Товар2   │ 00002   │ ...   ││
│ └────┴──────────┴─────────┴────────┘│
├──────────────────────────────────────┤
│ Отбор: [Поле] [ВидСравнения] [Знач] │
└──────────────────────────────────────┘

Свойства: ДинамическийСписок, Отбор, Сортировка, УсловноеОформление"#.to_string()),

        _ => Err(anyhow!("Неизвестный паттерн: {}. Используйте pattern=list для списка", pattern)),
    }
}
